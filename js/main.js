/* start serv 5 */


let modalViews = document.querySelectorAll(".serv-5  .cards .card .modal");
let modalBtns = document.querySelectorAll(".serv-5 .cards .card .button");
let modalClose = document.querySelectorAll(".serv-5 .cards .card .modal .content .close");


let modal = function(modalClick) {
    modalViews[modalClick].classList.add("active");
}

modalBtns.forEach((mb, i) =>{

    mb.addEventListener("click", () =>{

        modal(i);
    });
});

modalClose.forEach((mc) =>{

    mc.addEventListener("click", () =>{

        modalViews.forEach((mv) => {
            
            mv.classList.remove("active");
        });
    });
});



